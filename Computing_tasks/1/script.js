// Вот пример кода на JavaScript, который выводит пять строк из нулей, где количество нулей в каждой строке соответствует её номеру:

for (let i = 1; i <= 5; i++) {
  console.log('0'.repeat(i));
}


/*▎Объяснение:

1. Цикл for: Мы используем цикл for, чтобы пройтись по числам от 1 до 5 (включительно). Переменная i обозначает текущий номер строки.

2. Метод .repeat(): Метод строки .repeat(i) повторяет строку '0' ровно i раз.

3. console.log: Выводит результат на экран.
*/


for (let k = 1; k <= 5; k++) {
    let row = ''; // Создаем пустую строку для текущей строки
    for (let j = 1; j <= k; j++) { // Вложенный цикл для добавления нулей
      row += '0';
    }
    console.log(row); // Выводим строку с нулями
  }
  
/*▎Объяснение:

1. Внешний цикл for: Этот цикл отвечает за количество строк (от 1 до 5).

2. Вложенный цикл for: Внутри внешнего цикла запускается второй цикл, который выполняется i раз, чтобы добавить нужное количество нулей в строку.

3. Сбор строки row: В каждой итерации вложенного цикла мы добавляем '0' к строке row.

4. Вывод строки: После завершения вложенного цикла мы выводим строку row с нужным количеством нулей.
*/